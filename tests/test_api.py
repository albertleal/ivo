def test_openapi_schema_advertises_endpoints():
    """Swagger/OpenAPI schema is auto-generated by FastAPI."""
    cfg = Config(
        telegram=TelegramConfig(token="x", admin_chat_id=1),
        api=APIConfig(enable=True, port=0, allowed_ips=[]),
        adapters={},
        defaults=DefaultsConfig(adapter="copilot"),
        session=SessionConfig(backend="memory"),
        logging=LoggingConfig(),
    )
    catalog = build_catalog({}, {})
    ctx = BotContext(config=cfg, adapters={}, catalog=catalog,
                     sessions=SessionStore(backend="memory"))
    app = build_app(ctx, bot=MagicMock(send_message=AsyncMock()))
    c = TestClient(app)
    r = c.get("/openapi.json")
    assert r.status_code == 200
    paths = r.json()["paths"]
    assert "/health" in paths and "/models" in paths and "/send" in paths
